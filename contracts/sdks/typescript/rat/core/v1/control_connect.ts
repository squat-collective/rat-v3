//
// This is the operator counterpart to CapabilityInvokeService (invoke.proto): that
// service is the DATA plane (a plugin calls a capability it `requires`, C5-gated);
// this is the CONTROL plane (an operator adds/removes plugins). They are deliberately
// SEPARATE services with different authorization: capability-invocation is authorized
// from the caller's manifest `requires` (C5), whereas control is an OPERATOR action —
// `rat add` is a CLI, not a plugin with a manifest — so it does not flow through C5.
// In v1 the control listener's REACHABILITY is the authorization: a per-project unix
// socket (.rat/daemon.sock, ADR-023) means filesystem permissions gate who may
// register, exactly as they gate `rat down`. Operator identity + mTLS for a networked
// (TCP) control endpoint is deferred (ADR-027 Q01).
//
// This stays within the six-thing core: it mutates the Registry and exposes an admin
// RPC on the API gateway — two EXISTING responsibilities — and reuses the reconciler's
// unchanged convergence (it only makes the desired SET mutable). No 7th core thing.
//
// Status: v1 additive (ADR-027; bound by the ADR-017 pre-unfreeze gate). Core↔operator,
// not a plugin contract.

